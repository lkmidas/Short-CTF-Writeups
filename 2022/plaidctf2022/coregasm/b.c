#include <stdlib.h>
#include <stdio.h>

int main()
{
    // "info reg all" from gdb
    unsigned char st_regs_raw[8][10] = {
        { 0x00, 0x4d, 0x01, 0x1c, 0x58, 0xe7, 0x86, 0xfa, 0x02, 0x40 }, 
        { 0xf5, 0xbf, 0x8d, 0x80, 0x2b, 0xf2, 0xd4, 0xd6, 0x06, 0x40 }, 
        { 0xce, 0x05, 0xb0, 0xb4, 0xef, 0xa3, 0xe3, 0x9d, 0x0a, 0x40 }, 
        { 0x63, 0x9c, 0x83, 0x40, 0xaf, 0x09, 0x5f, 0xc1, 0x0d, 0x40 }, 
        { 0xbe, 0x52, 0x38, 0x72, 0x6f, 0x03, 0x70, 0xbd, 0x10, 0x40 }, 
        { 0x78, 0xb5, 0x2c, 0x15, 0xb4, 0x27, 0x20, 0x8b, 0x13, 0x40 }, 
        { 0x8f, 0x84, 0xf5, 0x89, 0x3c, 0xc9, 0x0a, 0x88, 0x15, 0x40 }, 
        { 0xb3, 0xc5, 0xf7, 0x22, 0x71, 0x64, 0xa4, 0x85, 0x16, 0x40 }, 
    };

    long double *st_regs_double[8];

    // Convert raw to doubles
    for (int i = 0; i < 8; i++)
        st_regs_double[i] = (long double *)(&st_regs_raw[i]);

    // 8 double values algorithm simplified:
    //  r[i] = to_double(0x3fff000000000000 | flag[i*6 : i*6 + 6]) + r[i - 1] (with r[-1] = 0)
    //  st[i] = r[i] * st[i - 1] (with st[-1] = 1)
    long double r[8];
    r[7] = *st_regs_double[0];
    for (int i = 0; i < 7; i++) {
        r[6 - i] = *st_regs_double[i + 1] / *st_regs_double[i];
    }

    long double flag[8];
    flag[0] = r[0];
    for (int i = 0; i < 7; i++) {
        flag[i + 1] = r[i + 1] - r[i];
    }

    for (int i = 0; i < 8; i++) 
        printf("%.32Lf\n", flag[i]);
}